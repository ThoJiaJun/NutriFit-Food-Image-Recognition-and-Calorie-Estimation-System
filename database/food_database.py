cursor = connection.cursor()

create_table_query = '''CREATE TABLE IF NOT EXISTS foods(
                        id INTEGER PRIMARY KEY AUTOINCREMENT,
                        name TEXT NOT NULL UNIQUE,
                        calories REAL,
                        carbs REAL,
                        protein REAL,
                        fats REAL)'''

cursor.execute(create_table_query)

cursor.executemany("""
INSERT INTO foods (name, calories, carbs, protein, fats)
VALUES (?, ?, ?, ?, ?)
ON CONFLICT(name) DO UPDATE SET
    calories=excluded.calories,
    carbs=excluded.carbs,
    protein=excluded.protein,
    fats=excluded.fats
""", foods)

connection.commit()

connection.close()